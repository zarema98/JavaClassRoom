package EverythingIsAnObject.exercise5;

public class DataOnly {
   private int i;
   private   double d;
   private   boolean b;

    public DataOnly(int i, double d, boolean b) {
        this.i = i;
        this.d = d;
        this.b = b;
    }

    public int getI() {
        return i;
    }

    public double getD() {
        return d;
    }

    public boolean getB() {
        return b;
    }
}
